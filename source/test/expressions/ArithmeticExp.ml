class ArithmeticExp {
        
    public static void main (String[] args) {
        
        a = 4 + 2 * 3;
        b = 4 + (2 * 3);
        c = (4 + 2) * 3;
        Console.write("a = " + a + "\n");   // a is 10
        Console.write("b = " + b + "\n");   // b is 10
        Console.write("c = " + c + "\n");   // c is 18
        Console.write("d = " + d + "\n");   // d is null

    }

}
